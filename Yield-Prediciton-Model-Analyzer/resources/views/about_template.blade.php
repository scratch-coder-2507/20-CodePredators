            <h2 id="heading">About Kisanmitra</h2>
            <p>Kisan is a machine learning based web application with expressive, elegant visualization of yield predictions. We believe development of a country is possible only with the technical advancement of farmers, an educated farmer can do way better than a experienced uneducated one, because he can use modern technology to make proper planning about his/her future. using kisanmitra , one can predict the crop yield for next 5 year.</p>
            <ul>
                <li><a href="https://kisanmitra.co/predict">Simple, fast Algorithm</a>.</li>
                <li>Expressive, intuitive visualization Using Charts.</li>
                <li><a href="https://kisanmitra.co/about">Robust background machine learning</a>.</li
                <li><a href="https://kisanmitra.com/team">Hardworking Team</a>.</li>
            </ul>
            <p>kisanmitra is accessible, yet powerful tools needed for crop yeild production. A superb combination of simplicity, elegance, and innovation give you tools you need to properly regulate your farming strategies according to environment and market.</p>
